class Bicycle(object):
	def __init__(self,name,weight,cost):
		self.name=name
		self.weight=weight
		self.cost=cost

class BikeShops(object):
	def __init__(self,name,inventory,margin):
		self.name=name
		self.inventory=inventory
		self.margin=margin
	def get_profit(self):
		sum(margin)

class Customers(object):
	def __init__(self,name,fund,own):
		self.name=name
		self.fund=fund
		self.own=own

